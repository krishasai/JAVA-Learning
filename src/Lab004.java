public class Lab004 {
    static public void main(String[]  args) {
        System.out.println("Helloooo");

    }

    public static class Lab008 {
        public static void main(String[] args) {
            //Data types :

            // primitive - Non Primitive

            //primitive - Numerical:
            //Byte -0 to 127

            byte a = 127;
            System.out.println(a);

            // Boolean - True or False
            boolean is_hacked = true;
            boolean is_nothacked =false;
            System.out.println(is_hacked);
            System.out.println(is_nothacked);

            //int - numbers 1,2,3.... upto two decimals
            int b = 777;
            System.out.println(b);

            //char - single letter A,B,C,D or Grades


            // short - 1234444
            short s= 12222;
            System.out.println(s);

            //long- 100,340,520,  morethan two decimals  or Phone number
            long length= 1000;
            System.out.println(length);

            //Primitive - Integral:
            //Float- 1.2, 10.5 .....upto one digit after the point
            float f= 1.7f;
            System.out.println(f);
            //Double - 100.11111 .... morethan one digit after the point
            double d= 101.111d;
            System.out.println(d);

            //Non Primitive:
            //String- a name or number or char - "sara","100".....(string cannot be converted into char)
            String strg="sara";
            System.out.println(strg);
        }
    }
}
